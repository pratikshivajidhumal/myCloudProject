package com.pojo;

/**
 *
 * @author pratik Dhumal
 */
public class DatabaseFilePojo 
{
    private String fileName;
    private String fileDescription;
    private String fileUploadTime;
    private String firstName;
    private String lastName;
    private String updateTime;
  

    /**
     * @return the fileName
     */
    public String getFileName() {
        return fileName;
    }

    /**
     * @param fileName the fileName to set
     */
    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    /**
     * @return the fileDescription
     */
    public String getFileDescription() {
        return fileDescription;
    }

    /**
     * @param fileDescription the fileDescription to set
     */
    public void setFileDescription(String fileDescription) {
        this.fileDescription = fileDescription;
    }

    /**
     * @return the fileUploadTime
     */
    public String getFileUploadTime() {
        return fileUploadTime;
    }

    /**
     * @param fileUploadTime the fileUploadTime to set
     */
    public void setFileUploadTime(String fileUploadTime) {
        this.fileUploadTime = fileUploadTime;
    }

    /**
     * @return the firstName
     */
    public String getFirstName() {
        return firstName;
    }

    /**
     * @param firstName the firstName to set
     */
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    /**
     * @return the lastName
     */
    public String getLastName() {
        return lastName;
    }

    /**
     * @param lastName the lastName to set
     */
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    /**
     * @return the updateTime
     */
    public String getUpdateTime() {
        return updateTime;
    }

    /**
     * @param updateTime the updateTime to set
     */
    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

  
    
    
}
